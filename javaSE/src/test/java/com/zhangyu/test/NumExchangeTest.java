package com.zhangyu.test;

/**
 * 两数互换
 */
public class NumExchangeTest {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        int i;
        i = a;
        a = b;
        b = i;
        System.out.println("a : " + a);
        System.out.println("b : " + b);
    }




}
